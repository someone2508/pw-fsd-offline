
// ====================== assignment operator ===========

let a = 10; // assignment operator ( = )

// +

a = a + 5;  // addition operator ( + )

a += 5;     // short-hand operator, additionAssignment

// -

a = a - 5;  // substraction operator ( - )

a -= 5;     // short-hand operator, substractionAssignment

// *

a = a * 5;  // multiple operator

a *= 5;     // short-hand operator

// /

a = a / 5;  // divide operator

a /= 5;     // short-hand operator, division assignment operator


